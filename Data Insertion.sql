-- Populating Mess table

BEGIN
    FOR rec IN (
        SELECT P.PAYMENT_ID, P.ERP_ID, R.HOSTEL_ID, P.AMOUNT
        FROM PAYMENTS P LEFT JOIN RESIDENTS R ON P.ERP_ID=R.ERP_ID
        WHERE PAYMENT_TYPE = 'Mess'
    ) LOOP
        DECLARE
            v_meals NUMBER;
        BEGIN
            -- Determine number of meals based on amount
            IF rec.AMOUNT = 5000 THEN
                v_meals := 3;
            ELSIF rec.AMOUNT = 3500 THEN
                v_meals := 2;
            ELSIF rec.AMOUNT = 2000 THEN
                v_meals := 1;
            ELSE
                -- Handle unexpected amounts
                v_meals := NULL;
                DBMS_OUTPUT.PUT_LINE('Unexpected amount ' || rec.AMOUNT || ' for PAYMENT_ID ' || rec.PAYMENT_ID);
            END IF;
            
            -- Only insert if meals are determined
            IF v_meals IS NOT NULL THEN
                INSERT INTO MESS (
                    ERP_ID, HOSTEL_ID, MESS_PERIOD, MEALS, PAYMENT_ID
                ) VALUES (
                    rec.ERP_ID,
                    rec.HOSTEL_ID,
                    'November 2024',
                    v_meals,
                    rec.PAYMENT_ID
                );
            END IF;
        END;
    END LOOP;
    COMMIT;
END;
/

-- Populating Laundry Table
BEGIN
    FOR rec IN (
        SELECT P.PAYMENT_ID AS pay_id, P.ERP_ID AS erp, R.HOSTEL_ID AS hid, P.AMOUNT AS amt
        FROM PAYMENTS P
        LEFT JOIN RESIDENTS R ON P.ERP_ID = R.ERP_ID
        WHERE P.PAYMENT_TYPE = 'Laundry'
    ) LOOP
        INSERT INTO LAUNDRY (HOSTEL_ID, ERP_ID, LAUNDRY_PERIOD, PAYMENT_ID)
        VALUES (rec.hid, rec.erp, 'Fall 2024', rec.pay_id);
    END LOOP;
END;

